#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	j;
	size_t	i;

	i = 0;
	j = ft_strlen(s);
	while (i < j)
	{
		(*f)(i, s + i);
		i++;
	}
}
